/**
 * nodemon
 * 可以监视应用的变化，它可以帮我们自动的启动应用
 *
 * 当你修改 node 文件时 nodemon 可以帮你立即执行，
 * 这样你可以马上看到修改后的文件执行的结果
 *
 * 安装
 * node install --save nodemon
 *
 * 安装后的文件位置
 * node_modules/.bin/nodemon
 *
 * 执行
 * ./node_modules/.bon/nodemon index.js
 *
 * 如果嫌麻烦可以在 package.json 里添加个命令
 * "scripts": {
 *   "customNodemoe": "./node_modules/.bon/nodemon index.js"
 * },
 * 然后执行时输入如下命令
 * npm customNodemoe
 */
